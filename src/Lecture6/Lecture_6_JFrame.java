    //ÖDEV : Girilen degeri ikilik tabana çeviren metodu yaz
    //kuyrukta bastan silinen elemanı , kuyrugun sonuna alan metod
    void bastanSilSonaEkle() {
        eleman tmp = k.bas;
        eleman son = k.son;
        while (tmp.ileri != son) {
            tmp = k.cikar();
            k.ekle(tmp);
        }
    }

    //hocanın yazdığı
    void basaAl() {
        eleman tmp = k.bas;
        eleman son = k.son;
        while (tmp.ileri != son) {
            tmp = k.cikar();
            k.ekle(tmp);
        }
    }

    //kuyruk sonundaki elemanı kuyrugun basına alma
    void sonuBasaAl() {
        eleman bas = k.cikar();
        if (bas != null) {
            k.ekle(bas);
        }
    }

    //hocanın yazdığı
    void sonaAl() {
        eleman bas = k.cikar();
        if (bas != null) {
            k.ekle(bas);
        }
    }

