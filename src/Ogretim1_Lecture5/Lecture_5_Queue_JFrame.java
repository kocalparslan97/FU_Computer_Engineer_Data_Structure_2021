    //kuyrugun basındaki elemanı kuyrugun sonuna alan metod
    void sonaAl() {
        eleman bas = k.cikar();
        if (bas != null) {
            k.ekle(bas);
        }
    }

    //kuyrugun sonundaki elemanı kuyrugun basına alan metod
    void basaAl() {
        eleman tmp = k.bas;
        eleman son = k.son;
        while (tmp.ileri != son) {
            tmp = k.cikar();
            k.ekle(tmp);
            //1-2-3-4 -> kuyrugun normal hali
            //2-3-4-1
            //3-4-1-2
            //4-1-2-3
        }
    }

    //ODEV : Kuyruk kullanılarak 10 sayısını  girersem 10 sayısına kadar
    //ikilik tabanda sayıları gosteren metod -Normalde 5 puan aldım ama
    //İÖ olduğum için sayılmadı :(
    private String ikiligeCevir(int sayi) {
        eleman tmp = k.bas;
        String S = "";
        while (sayi > 0) {
            int kalan = (sayi % 2);
            sayi = sayi / 2;
            k.ekle(new eleman(kalan));
        }

        while (!k.bosmu()) {
            eleman e = k.cikar();
            S = S + "(" + e.icerik + ")";
        }
        return S;
    }

    //kuyruktaki en buyuk elemanı bulan ve kuyruk yapısını değiştirmeyen
    //metod
    int enBuyukBul() {
        eleman tmp = k.bas;
        eleman son = k.son;
        int enb = tmp.icerik;
        while (tmp != son) {
            if (tmp.icerik > enb) {
                enb = tmp.icerik;
            }
            tmp = k.cikar();
            k.ekle(tmp);
        }
        if (tmp.icerik > enb) {
            enb = tmp.icerik;
        }
        return enb;
    }

